const deriveThumbnail = (path, overrides = {}) => {
  if (!path) return null;
  if (path.includes('/thumbs/')) return path;
  if (path.startsWith('/backgrounds/')) {
    return path.replace('/backgrounds/', '/backgrounds/thumbs/');
  }
  return overrides[path] || path;
};

const resolveRenderableUrl = (maybeUrl, filePath = null) => {
  const url = (typeof maybeUrl === 'string') ? maybeUrl : null;
  const fp = (typeof filePath === 'string' && filePath) ? filePath : null;

  // If we have an absolute file path, never hand it directly to the WebView.
  // Convert it to an asset URL when possible; otherwise return null so callers
  // can use a placeholder until the bridge is ready.
  const absolute = fp || (url && isAbsoluteFilePath(url) ? url : null);
  if (absolute) {
    if (typeof window !== 'undefined' && window?.tauriAPI?.filePathToAssetUrl) {
      try {
        return toSafeUrl(window.tauriAPI.filePathToAssetUrl(absolute));
      } catch {
        return null;
      }
    }
    return null;
  }

  return toSafeUrl(url);
};

const normalizeCategory = (category) => ({
  ...category,
  images: (category.images || []).map((image) => {
    const base = image.path || image.fullSizePath || image;
    const name = image.name || base?.split('/').pop();
    const path = image.path || base;

    const isAbs = isAbsoluteFilePath(path);
    const hasAssetBridge =
      typeof window !== 'undefined' &&
      isAbs &&
      window?.tauriAPI?.filePathToAssetUrl;

    // Desktop: convert absolute on-disk paths (e.g. /System/Library/...) into
    // WebView-loadable asset URLs. If the bridge isn't ready yet, do NOT keep
    // the raw absolute path in the <img> tag (WebView will reject it).
    const convertedPath = hasAssetBridge
      ? window.tauriAPI.filePathToAssetUrl(path)
      : (isAbs ? null : path);

    // Keep the original file path around for stable selection/transcode.
    const filePath = isAbs ? path : (image.filePath || null);

    return {
      ...image,
      name,
      filePath,
      path: toSafeUrl(convertedPath),
      // NOTE: for absolute file paths we do not try to derive '/thumbs/' variants.
      thumbPath: toSafeUrl(
        hasAssetBridge
          ? convertedPath
          : (convertedPath ? deriveThumbnail(image.thumbPath || convertedPath) : null)
      ),
    };
  }),
});

